  void _copySettlement(BuildContext context) {
    String message = 'ðŸŽ¯ *SETTLEMENT PLAN* ðŸŽ¯\n\n';
    message += '*${group.name}*\n\n';

    if (settlements.isEmpty) {
      message += 'âœ… Sab settle hai!\n\n';
    } else {
      message += 'Minimum ${settlements.length} transaction${settlements.length > 1 ? 's' : ''}:\n\n';
      for (int i = 0; i < settlements.length; i++) {
        final s = settlements[i];
        final fromUser = members.firstWhere((m) => m.uid == s.fromUserId);
        final toUser = members.firstWhere((m) => m.uid == s.toUserId);
        message += '${i + 1}. ${fromUser.avatar} ${fromUser.name} â†’ ${toUser.avatar} ${toUser.name}\n';
        message += '   â‚¹${s.amount.toStringAsFixed(2)}\n\n';
      }
    }

    message += '_Generated by MUKHLA ðŸš€_';

    Clipboard.setData(ClipboardData(text: message));
    ScaffoldMessenger.of(context).showSnackBar(
      const SnackBar(
        content: Text('Copied to clipboard! ðŸ“‹'),
        backgroundColor: Colors.green,
      ),
    );
  }
